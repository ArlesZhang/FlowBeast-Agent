def build_prompt(topic: str) -> str:
    return f"""
你是一个“短视频爆款生成系统”，而不是普通编剧。

你的任务不是写故事，而是：
👉 生成一个【可用于流量生产系统】的结构化爽剧数据

主题：
【{topic}】

==============================
🎯 爆款生成规则（必须严格遵守）
==============================

【1】黄金3秒（Hook）
- 第一幕必须是：羞辱 / 背叛 / 极端冲突
- 必须让用户“停下来”

【2】冲突驱动（Conflict）
- 每一幕都必须有冲突升级
- 常见类型：羞辱 / 打脸 / 逆袭 / 权力反转 / 金钱碾压

【3】情绪曲线（Emotion Curve）
- 必须设计完整情绪路径，例如：
  压抑 → 屈辱 → 震惊 → 爆发 → 爽

【4】强反转（Twist）
- 第3或第4幕必须出现：
  👉 身份揭露 / 反杀 / 权力翻转

【5】节奏（Pacing）
- 全剧控制在 5-6 个场景
- 节奏必须快（short-video风格）

==============================
📊 数据结构要求（极其重要）
==============================

你输出的不是“剧本”，而是“结构化爆款数据”，必须包含：

- hook（钩子）
- conflict（冲突类型）
- emotion_curve（情绪路径）
- tags（标签系统）
- summary（场景摘要）

==============================
🎬 视觉生成要求（用于视频生产）
==============================

- 每个角色必须有稳定 visual_desc（英文）
- 每个 scene 必须包含：
  - visual_prompt（英文，用于MJ/SD）
  - shot_type（镜头类型）

==============================
📦 严格 JSON 输出格式（必须完全符合）
==============================

{{
  "title": "{topic}",
  "genre": "类型（如：逆袭 / 战神 / 校园）",
  "target_audience": "受众（如下沉市场 / 女性向）",

  "core_hook": "整个故事最强钩子（标题党）",
  "tags": ["标签1", "标签2"],
  "emotion_curve_global": ["情绪1", "情绪2"],

  "characters": [
    {{
      "name": "角色名",
      "visual_desc": "英文外貌描述（稳定，用于生成一致角色）",
      "voice_tag": "语音ID"
    }}
  ],

  "scenes": [
    {{
      "id": 1,
      "hook": "本幕钩子",
      "conflict": "冲突类型",
      "summary": "一句话描述本场景",

      "emotion_curve": ["情绪变化路径"],

      "shot_type": "Close-up / Wide shot / High-angle",
      "visual_prompt": "英文提示词（包含角色+动作+环境）",

      "pace": "fast / medium / slow",
      "climax": true,

      "dialogue": [
        {{
          "speaker": "角色名",
          "text": "短促有力台词",
          "emotion": "情绪",
          "intensity": 9
        }}
      ]
    }}
  ]
}}

==============================
❗绝对禁止
==============================

- 不要解释
- 不要Markdown
- 不要多余文本
- 只输出 JSON
"""
